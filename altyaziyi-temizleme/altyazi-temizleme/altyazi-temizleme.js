// altyazi-temizleme.js
// YouTube otomatik altyazılarını (ASR) kullanıcının mantığıyla temizleyen algoritma

window.DoxmaximaSubCleaner = {
    splitLongText: function(text, maxChars = 55) {
        if (!text || text.length <= maxChars) return text;
        
        const words = text.split(' ');
        let result = '';
        let currentLine = '';

        for (let i = 0; i < words.length; i++) {
            const word = words[i];
            if ((currentLine + word).length > maxChars) {
                if (currentLine.length > 0) {
                    result += currentLine.trim() + '\n';
                    currentLine = '';
                }
            }
            currentLine += word + ' ';
        }
        if (currentLine.trim().length > 0) {
            result += currentLine.trim();
        }
        return result;
    },

    isAutoGenerated: function(vttText) {
        // Otomatik oluşturulan VTT'lerde genellikle <c> etiketleri (kelime kelime senkron için)
        // ve "align:start position:0%" pozisyonlamaları çok yoğun bulunur.
        // Ayrıca <00:00:00.000> formatında satır içi zaman damgaları (inline timestamps) vardır.
        // Manuel yüklenmiş temiz VTT'lerde bunlar genelde olmaz.
        const hasCTags = vttText.includes('</c>');
        const hasAlignStart = vttText.includes('align:start position:0%');
        const hasInlineTimestamps = /<\d{2}:\d{2}:\d{2}\.\d{3}>/.test(vttText);
        
        return hasCTags || hasAlignStart || hasInlineTimestamps;
    },

    cleanAutoGeneratedVtt: function(vttText) {
        // Eğer zaten temiz (manuel) bir altyazıysa, zamanlamayı bozmamak için blok mantığına dokunmuyoruz.
        // Ancak FAZLA BOŞLUKLARI ve UZUN SATIRLARI yine de temizliyoruz!
        if (!this.isAutoGenerated(vttText)) {
            console.log("[Doxmaxima] Bu altyazı zaten yayıncı tarafından temizlenmiş, sadece boşluk/satır düzeltmesi yapılıyor.");
            
            const rawLines = vttText.split('\n');
            let inBlock = false;
            for(let i=0; i<rawLines.length; i++){
                let ln = rawLines[i];
                if(ln.includes('-->')){
                    inBlock = true;
                } else if(ln.trim() === ''){
                    inBlock = false;
                } else if(inBlock && !ln.includes('WEBVTT') && !ln.includes('Kind:') && !ln.includes('Language:')) {
                    // Metin satırı: HTML boşluklarını ve sıfır genişlikli karakterleri temizle
                    ln = ln.replace(/&nbsp;/gi, ' ').replace(/&#160;/gi, ' ').replace(/[\u200B-\u200D\uFEFF]/g, '');
                    // Çoklu boşlukları tek boşluğa indir
                    ln = ln.replace(/\s+/g, ' ').trim();
                    // Uzun satırsa böl
                    rawLines[i] = this.splitLongText(ln, 55);
                }
            }
            
            return {
                text: rawLines.join('\n'),
                wasAlreadyClean: true
            };
        }

        const lines = vttText.split('\n');
        const blocks = [];
        let currentBlock = null;
        let header = "";
        let isHeader = true;

        for (let i = 0; i < lines.length; i++) {
            let line = lines[i].trim();
            
            if (isHeader) {
                if (line.includes('-->')) {
                    isHeader = false;
                } else {
                    if (line) header += line + '\n';
                    continue;
                }
            }

            if (line.includes('-->')) {
                if (currentBlock) blocks.push(currentBlock);
                const parts = line.split('-->');
                currentBlock = {
                    start: parts[0].trim(),
                    // "00:00:02.149 align:start..." kısmından sadece saati temizle
                    end: parts[1].split(' ')[0].trim(), 
                    textLines: []
                };
            } else if (line !== '' && currentBlock) {
                // <c> etiketlerini vb. html taglarını temizle
                const cleanLine = line.replace(/<[^>]+>/g, '').trim();
                // Sadece kelime/harf içeren boş olmayan satırları al
                if (cleanLine) {
                    currentBlock.textLines.push(cleanLine);
                }
            }
        }
        if (currentBlock) blocks.push(currentBlock);

        // Kullanıcının tarif ettiği blok birleştirme (i, i+1, i+2) mantığı
        let cleanVtt = header + "\n";
        
        for (let i = 0; i < blocks.length; i += 2) {
            const b1 = blocks[i];
            const b2 = blocks[i+1];
            const b3 = blocks[i+2];

            if (!b1) continue;

            let startTime = b1.start;
            let text = b1.textLines.join(' ');
            let endTime = b1.end;

            if (b2) {
                text = b2.textLines.join(' '); // 2. bloğun textini alıyoruz
                if (b3) {
                    endTime = b3.start; // 3. bloğun başlangıcını alıyoruz
                } else {
                    endTime = b2.end;
                }
            }

            if (text) {
                // HTML özel boşluk karakterlerini ve görünmez karakterleri temizle
                text = text.replace(/&nbsp;/gi, ' ').replace(/&#160;/gi, ' ').replace(/[\u200B-\u200D\uFEFF]/g, '');
                // Çoklu boşlukları tek boşluğa indir
                text = text.replace(/\s+/g, ' ').trim();
                text = this.splitLongText(text, 55);
                cleanVtt += `${startTime} --> ${endTime}\n${text}\n\n`;
            }
        }

        return {
            text: cleanVtt.trim() + "\n",
            wasAlreadyClean: false
        };
    },

    // YOUTUBE PLAYER ENTEGRASYONU İÇİN ÇEVİRİCİLER
    json3ToVtt: function(json3Text) {
        try {
            const data = JSON.parse(json3Text);
            let vtt = "WEBVTT\nKind: captions\nLanguage: en\n\n";
            
            function formatTime(ms) {
                let s = ms / 1000;
                let h = Math.floor(s / 3600);
                s %= 3600;
                let m = Math.floor(s / 60);
                s %= 60;
                let msStr = Math.floor((s - Math.floor(s)) * 1000).toString().padStart(3, '0');
                return `${h.toString().padStart(2,'0')}:${m.toString().padStart(2,'0')}:${Math.floor(s).toString().padStart(2,'0')}.${msStr}`;
            }

            if (data.events) {
                for (let ev of data.events) {
                    if (!ev.segs || typeof ev.dDurationMs === 'undefined') continue;
                    let startStr = formatTime(ev.tStartMs || 0);
                    let endStr = formatTime((ev.tStartMs || 0) + ev.dDurationMs);
                    let text = ev.segs.map(s => s.utf8).join('');
                    if (text.trim() === "") continue;
                    vtt += `${startStr} --> ${endStr}\n${text}\n\n`;
                }
            }
            return vtt;
        } catch(e) {
            console.error("[Doxmaxima] JSON3 -> VTT dönüşüm hatası:", e);
            return "";
        }
    },

    vttToJson3: function(vttText) {
        try {
            const lines = vttText.split('\n');
            const events = [];
            let currentEvent = null;
            
            function parseTime(timeStr) {
                // Saat kısmı olmayan VTT'leri de desteklemek için dinamik ayırıcı
                const parts = timeStr.split(':');
                const secParts = parts.pop().split('.');
                let ms = parseInt(secParts[1], 10);
                let s = parseInt(secParts[0], 10);
                let m = parts.length > 0 ? parseInt(parts.pop(), 10) : 0;
                let h = parts.length > 0 ? parseInt(parts.pop(), 10) : 0;
                
                return (h * 3600 + m * 60 + s) * 1000 + ms;
            }

            for (let i = 0; i < lines.length; i++) {
                let line = lines[i].trim();
                if (line.includes('-->')) {
                    const parts = line.split('-->');
                    const startMs = parseTime(parts[0].trim());
                    const endMs = parseTime(parts[1].trim());
                    currentEvent = {
                        tStartMs: startMs,
                        dDurationMs: endMs - startMs,
                        segs: [{ utf8: "" }]
                    };
                    events.push(currentEvent);
                } else if (line !== '' && !line.includes('WEBVTT') && !line.includes('Kind:') && !line.includes('Language:') && currentEvent) {
                    if (currentEvent.segs[0].utf8 !== "") {
                        currentEvent.segs[0].utf8 += "\n";
                    }
                    currentEvent.segs[0].utf8 += line;
                }
            }
            return JSON.stringify({ wireMagic: "pb3", events: events });
        } catch(e) {
            console.error("[Doxmaxima] VTT -> JSON3 dönüşüm hatası:", e);
            return "{}";
        }
    }
};
